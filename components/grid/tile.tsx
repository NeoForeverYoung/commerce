import clsx from 'clsx';
import Image from 'next/image';
import Label from '../label';

export function GridTileImage({
  isInteractive = true,
  active,
  label,
  ...props  // ...props means all the properties that are part of the React.ComponentProps<typeof Image>, which include any props that the Image component accepts.
}: {
  isInteractive?: boolean;
  active?: boolean;
  label?: {
    title: string;
    amount: string;
    currencyCode: string;
    position?: 'bottom' | 'center';
  };
} & React.ComponentProps<typeof Image>) { // The expression & React.ComponentProps<typeof Image> is a TypeScript type intersection that combines your custom prop types with all the prop types of the Image component.
  return (
    <div
      // The object { ... } within clsx specifies classes that depend on the component's props. Every div is supposed to have a className. Good to know this basic.

      className={clsx(
        'group flex h-full w-full items-center justify-center overflow-hidden rounded-lg border bg-white hover:border-blue-600 dark:bg-black',
        {
          relative: label,
          'border-2 border-blue-600': active,
          'border-neutral-200 dark:border-neutral-800': !active
        }
      )}
    >
      {props.src ? (
        // eslint-disable-next-line jsx-a11y/alt-text -- `alt` is inherited from `props`, which is being enforced with TypeScript.
        <Image
          // The className attribute in the <Image> element is essential for applying CSS classes to the element, which controls its style and layout. 
          className={clsx('relative h-full w-full object-contain', {
            'transition duration-300 ease-in-out group-hover:scale-105': isInteractive
          })}
          {...props}
        />
      ) : null}
      {label ? (
        // {label ? (...) :null} is the ternary operator.
        <Label
          title={label.title}
          amount={label.amount}
          currencyCode={label.currencyCode}
          position={label.position}
        />
      ) : null}
    </div>
  );
}
